t = int(input())
while t:
    n = int(input())
    s = input()
    c1,c2,c3,c4 = 0,0,0,0
    ans = 0
    for i in s:
        if i == 'A':
            c1 += 1
        elif i == 'B':
            c2 += 1
        elif i == 'C':
            c3 += 1
        elif i == 'D':
            c4 += 1
    ans += min(c1,n)
    ans += min(c2,n)
    ans += min(c3,n)
    ans += min(c4,n)
    print(ans)
    t -=1