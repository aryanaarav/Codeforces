t = int(input())
while t:
    n = int(input())
    l = list(map(int, input().split()))
    c = [0]*(n+1)
    for i in l:
        c[i] += 1
    f = False
    for j in c[1:]:
        f  = f or j%2 == 1
    print("YES" if f else "NO")
    t -=1